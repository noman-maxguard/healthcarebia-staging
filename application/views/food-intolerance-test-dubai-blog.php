          <h5>A Smart, Four Week Game Plan</h5>

          <p><strong>Week 0, Baseline &amp; Setup</strong></p>
          <ul class="ingredients-list">
            <li>Keep a 5–7 day food and symptom log (time, foods, symptoms, stress/sleep).</li>
            <li>Do core tests, breath test (if lactose/fructose/SIBO suspected), celiac screen, and basic nutrients.</li>
            <li>Stock easy swaps, lactose free dairy, low FODMAP fruits (berries), rice or GF wraps, olive oil, eggs, lean proteins.</li>
          </ul>

          <p><strong>Weeks 1–2, Targeted Elimination (Short &amp; Focused)</strong></p>
          <ul class="ingredients-list">
            <li>Remove the top 2–4 suspects only (e.g., lactose, high FODMAP onion/garlic, excess fructose, or gluten if celiac is confirmed).</li>
            <li>Keep meals simple, protein + low FODMAP veg + tolerated carb + healthy fat.</li>
            <li>Hydrate well; aim for 7–8 hours sleep. Stress management matters.</li>
          </ul>

          <p><strong>Week 3, Re Challenge (One Variable at a Time)</strong></p>
          <ul class="ingredients-list">
            <li>Reintroduce one item every 2–3 days in a controlled portion. Track symptoms for 48 hours.</li>
            <li>If a food triggers symptoms, note the dose that causes issues, many people tolerate small amounts.</li>
          </ul>

          <p><strong>Week 4, Personalisation &amp; Expansion</strong></p>
          <ul class="ingredients-list">
            <li>Build your lists, “green” (safe), “yellow” (dose dependent), “red” (avoid for now).</li>
            <li>Rebalance protein, fibre, omega 3s, and minerals.</li>
            <li>If labs were low, correct with food first; consider supplements only if needed.</li>
          </ul>

