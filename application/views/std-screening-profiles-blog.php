                        <li><strong>Lower the risk of pelvic inflammatory disease and infertility:</strong> which can follow untreated infections.</li>
                        <li><strong>Make informed decisions:</strong> about contraception, pregnancy, and your sexual and reproductive health.</li>
                    </ul>

