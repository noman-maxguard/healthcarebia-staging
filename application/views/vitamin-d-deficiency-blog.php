                        <li>Anyone with indoor jobs, sun avoidance, covered skin, darker skin, or chronic health conditions should test.</li>
                        <li>The standard <strong>25-hydroxyvitamin D blood test</strong> can be done at home or clinic by a DHA-licensed nurse.</li>
                        <li>Once a year is enough for most adults. Those with risk factors or on supplements may need 6-monthly or quarterly checks.</li>
                    </ul>
