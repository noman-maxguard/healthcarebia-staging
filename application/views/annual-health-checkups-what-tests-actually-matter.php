                                <li>Pap smear/HPV per schedule</li>
                                <li>Targeted labs: CBC, CMP, TSH, vitamin D, iron/ferritin, B12</li>
                            </ul>
                        </li>
                        <li><strong>40s</strong>
                            <ul class="ingredients-list">
                                <li>Above + start colorectal screening (FIT or colonoscopy)</li>
                                <li>Consider mammogram (women), eye pressure checks</li>
                            </ul>
                        </li>
                        <li><strong>50s–60s+</strong>
                            <ul class="ingredients-list">
