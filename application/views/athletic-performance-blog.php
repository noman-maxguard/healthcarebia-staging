                    <h5>Why Athletes in Dubai Need More Than Water and Tablets</h5>
                    <ol>
                        <li><strong>Extreme sweat loss:</strong> A UAE field study measured sweat rates above 1.5 litres per hour when the heat index passed 50°C. Athletes doing intervals can lose even more.</li>
                        <li><strong>Salt-rich sweat:</strong> Each liter carries four to six grams of sodium. A typical sports tablet holds about one gram, so deficits build fast, leading to cramps and early fatigue.</li>
                        <li><strong>Travel dehydration:</strong> Cabin air is dry. A six-hour flight to Dubai leaves visitors roughly one liter short of fluid before they even lace up.</li>
                    </ol>
