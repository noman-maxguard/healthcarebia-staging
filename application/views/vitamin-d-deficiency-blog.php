                    <h5>What to Do If Your Levels Are Low</h5>
                    <ul class="ingredients-list">
                        <li><strong>Below 20 ng/mL:</strong> Your doctor may recommend supplements, diet changes, or supervised sun exposure.</li>
                        <li><strong>20–30 ng/mL:</strong> Usually needs annual testing and minor lifestyle adjustments.</li>
                        <li><strong>30–50 ng/mL:</strong> Maintain with normal sun and diet.</li>
                        <li><strong>Over 100 ng/mL:</strong> Too high—stop supplements and consult your doctor.</li>
                    </ul>

                    <h5>How to Boost Vitamin D Levels Safely</h5>
                    <ol>
                        <li><strong>Sun exposure:</strong> Spend 10–30 minutes in midday sun, arms and face uncovered, 2–3 times per week. Avoid burning.</li>
                        <li><strong>Diet:</strong> Eat fatty fish (salmon, sardines), eggs, fortified milk/plant milks, and mushrooms exposed to UV.</li>
                        <li><strong>Supplements:</strong> D3 (cholecalciferol) is preferred. Usual dose is 1,000–2,000 IU daily, but your doctor may prescribe higher doses for deficiency.</li>
                        <li><strong>Follow-up:</strong> Retest after 8–12 weeks to make sure your levels are improving. Adjust as needed with your doctor.</li>
                    </ol>

