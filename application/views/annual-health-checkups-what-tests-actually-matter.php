                    <!-- At-home care -->
                    <h5>When At-Home Care Makes Sense</h5>
                    <p>If you have a tight schedule, mobility limits, caregiving duties, or prefer privacy, at-home healthcare is a strong option. A nurse can collect blood for your panels, record vitals, and arrange tele-consults. Many people find it easier to stay consistent when care comes to them.</p>

                    <!-- Safety -->
                    <h5>Safety, Side Effects &amp; Follow-Up</h5>
                    <ul class="tick-list">
                        <li><strong>Blood draws:</strong> may cause brief soreness or a small bruise; side effects are usually mild and short-lived.</li>
                        <li><strong>Imaging:</strong> has risks and benefits—discuss with your clinician which tests fit your profile.</li>
                        <li><strong>Abnormal results:</strong> may prompt repeat labs, additional testing, or referral.</li>
                        <li><strong>Guidelines:</strong> clinicians reference global and local public-health bodies; recommendations vary by country and medical conditions—personalise your plan.</li>
                    </ul>

